"""
auto_label.py — Fuzzy Posture Labeling
=======================================
Worker Pose Safety Monitoring System — Production Pipeline

Reads dataset.csv (generated by dataset_generator_yolo.py) and assigns
posture labels using FUZZY SCORING instead of rigid thresholds.

Fuzzy Labeling Logic:
  Each angle contributes a continuous score in [0, 1] using a sigmoid-based
  membership function. Scores are weighted and summed → final classification.

  back_angle  (degrees from vertical, 0° = upright):
    SAFE zone    : < 20°
    MODERATE zone: 20–45°
    UNSAFE zone  : > 40°

  knee_angle  (flexion, 180° = fully extended):
    SAFE zone    : > 155°  (standing/slightly bent)
    MODERATE zone: 110–155°
    UNSAFE zone  : < 110°  (deep squat without support)

  neck_angle  (tilt, 180° = perfectly aligned):
    SAFE zone    : > 160°
    MODERATE zone: 130–160°
    UNSAFE zone  : < 130°

  Combined weighted score:
    total_score = 0.50 * back_score + 0.30 * knee_score + 0.20 * neck_score
    safe     if total_score < 0.33
    moderate if 0.33 <= total_score < 0.65
    unsafe   if total_score >= 0.65

  8% random label noise applied to reduce rule-memorisation overfitting.

Output: labeled_dataset.csv

Usage:
  python auto_label.py
"""

import random
from pathlib import Path

import numpy as np
import pandas as pd

# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

# Seed for reproducible label noise
random.seed(42)
np.random.seed(42)

LABEL_NOISE_PROB: float = 0.08   # 8% random label flip
LABELS: list = ["safe", "moderate", "unsafe"]

# Feature columns used for labeling (others are kept but not used)
ANGLE_COLS: list = ["back_angle", "knee_angle", "neck_angle", "elbow_angle"]
ALL_FEATURE_COLS: list = [
    "back_angle", "knee_angle", "neck_angle", "elbow_angle",
    "back_vel", "knee_vel", "neck_vel",
    "back_acc", "knee_acc",
    "norm_shoulder_x", "norm_shoulder_y",
    "norm_hip_x", "norm_hip_y",
    "norm_knee_x", "norm_knee_y",
]


# ---------------------------------------------------------------------------
# Fuzzy Membership Functions
# ---------------------------------------------------------------------------

def _sigmoid(x: float, center: float, steepness: float = 0.15) -> float:
    """
    Sigmoid function mapping x to [0, 1].
    Returns values close to 0 for x << center and close to 1 for x >> center.
    steepness controls how sharp the transition is.
    """
    return float(1.0 / (1.0 + np.exp(-steepness * (x - center))))


def back_angle_score(back_angle: float) -> float:
    """
    Compute a risk score in [0, 1] for the back angle.

    back_angle = angle from vertical (0° = upright, 90° = fully bent forward).

    Score progression:
      0° → 0.0  (perfectly safe)
      20° → ~0.2 (low risk)
      35° → ~0.5 (moderate)
      50°+ → ~0.9+ (high risk)
    """
    if not np.isfinite(back_angle):
        return 0.5  # neutral if unknown
    # Sigmoid centered at 32° (midpoint of moderate zone)
    return _sigmoid(back_angle, center=32.0, steepness=0.12)


def knee_angle_score(knee_angle: float) -> float:
    """
    Compute a risk score in [0, 1] for the knee flexion angle.

    knee_angle = angle at knee joint (180° = extended, lower = more bent).

    Score progression (INVERTED — lower angle = higher risk):
      180° → 0.0 (fully extended, safe)
      155° → ~0.1
      130° → ~0.4
      110° → ~0.7 (risky without proper support)
      90°  → ~0.9
    """
    if not np.isfinite(knee_angle):
        return 0.3  # mild risk if unknown
    # Invert: high angle → low risk; use 1 - sigmoid
    return 1.0 - _sigmoid(knee_angle, center=132.0, steepness=0.10)


def neck_angle_score(neck_angle: float) -> float:
    """
    Compute a risk score in [0, 1] for the neck tilt angle.

    neck_angle = angle at shoulder between head-direction and torso-direction.
    180° = head aligned, lower = forward head / neck tilt.

    Score progression (INVERTED):
      180° → 0.0
      160° → ~0.1
      145° → ~0.4
      130° → ~0.7
    """
    if not np.isfinite(neck_angle):
        return 0.2  # mild risk if unknown
    return 1.0 - _sigmoid(neck_angle, center=145.0, steepness=0.10)


def compute_fuzzy_label(
    back_angle: float,
    knee_angle: float,
    neck_angle: float,
) -> str:
    """
    Assign a posture label using weighted fuzzy scoring.

    Each angle is converted to a continuous risk score in [0, 1].
    Weighted sum determines the final class.

    Weights:
      back  : 0.50  (primary ergonomic risk factor)
      knee  : 0.30  (secondary — important for lifting)
      neck  : 0.20  (tertiary — head/neck alignment)

    Boundaries:
      total_score < 0.33  → safe
      0.33 – 0.65         → moderate
      > 0.65              → unsafe

    An 8% random label-flip noise is applied to prevent
    the classifier from memorising exact threshold rules.
    """
    # Individual risk scores
    b_score = back_angle_score(back_angle)
    k_score = knee_angle_score(knee_angle)
    n_score = neck_angle_score(neck_angle)

    # Weighted combination
    total = 0.50 * b_score + 0.30 * k_score + 0.20 * n_score

    # Classify
    if total < 0.33:
        label = "safe"
    elif total < 0.65:
        label = "moderate"
    else:
        label = "unsafe"

    # Apply random label noise (8% chance) to soften rigid rule boundaries
    if random.random() < LABEL_NOISE_PROB:
        label = random.choice(LABELS)

    return label


def is_empty_label(value) -> bool:
    """Return True when the label cell is blank or NaN."""
    if pd.isna(value):
        return True
    return str(value).strip() == ""


# ---------------------------------------------------------------------------
# Main Entry Point
# ---------------------------------------------------------------------------

def main() -> None:
    base_dir = Path(__file__).resolve().parent
    input_csv = base_dir / "dataset.csv"
    output_csv = base_dir / "labeled_dataset.csv"

    if not input_csv.exists():
        raise FileNotFoundError(
            f"Input dataset not found: {input_csv}\n"
            "Run dataset_generator_yolo.py first."
        )

    df = pd.read_csv(input_csv)

    # --- Validate required columns ---
    required = {"video_name", "frame_id", "back_angle", "knee_angle", "neck_angle", "label"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"Missing required columns in dataset.csv: {sorted(missing)}")

    # --- Convert angle columns to numeric ---
    for col in ANGLE_COLS:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")

    # --- Convert all feature columns to numeric ---
    for col in ALL_FEATURE_COLS:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")

    # --- Drop rows where any primary angle is NaN ---
    primary_angles = ["back_angle", "knee_angle", "neck_angle"]
    initial_count = len(df)
    df = df.dropna(subset=primary_angles)
    dropped = initial_count - len(df)
    if dropped > 0:
        print(f"[INFO] Dropped {dropped} rows with NaN in primary angles")

    # --- Ensure label column holds strings ---
    df["label"] = df["label"].astype("string")

    # --- Apply fuzzy labeling to unlabeled rows ---
    empty_mask = df["label"].apply(is_empty_label)
    rows_to_label = df.loc[
        empty_mask, ["back_angle", "knee_angle", "neck_angle"]
    ]

    df.loc[empty_mask, "label"] = rows_to_label.apply(
        lambda row: compute_fuzzy_label(
            back_angle=float(row["back_angle"]),
            knee_angle=float(row["knee_angle"]),
            neck_angle=float(row["neck_angle"]),
        ),
        axis=1,
    )

    # --- Save output ---
    df.to_csv(output_csv, index=False)

    # --- Print statistics ---
    labeled_count = int(empty_mask.sum())
    total = len(df)

    # Per-class counts for newly labeled rows
    new_labels = df.loc[empty_mask, "label"].str.lower()
    safe_n = int((new_labels == "safe").sum())
    moderate_n = int((new_labels == "moderate").sum())
    unsafe_n = int((new_labels == "unsafe").sum())

    # Overall distribution
    all_labels = df["label"].str.lower()
    all_safe = int((all_labels == "safe").sum())
    all_mod = int((all_labels == "moderate").sum())
    all_unsafe = int((all_labels == "unsafe").sum())

    print(f"\n{'='*55}")
    print(f"Fuzzy Auto-Labeling Results (noise={LABEL_NOISE_PROB*100:.0f}%)")
    print(f"{'='*55}")
    print(f"Total rows in dataset : {total}")
    print(f"Rows labeled this run : {labeled_count}")
    print(f"  SAFE     : {safe_n}")
    print(f"  MODERATE : {moderate_n}")
    print(f"  UNSAFE   : {unsafe_n}")
    print(f"\nOverall Distribution")
    print(f"  SAFE     : {all_safe:5d}  ({100*all_safe/total:.1f}%)")
    print(f"  MODERATE : {all_mod:5d}  ({100*all_mod/total:.1f}%)")
    print(f"  UNSAFE   : {all_unsafe:5d}  ({100*all_unsafe/total:.1f}%)")
    print(f"\nLabeled dataset saved : {output_csv}")
    print(f"\nNext step: run  python train_model.py")


if __name__ == "__main__":
    main()
