"""
auto_label.py — Ergonomic Rule-Based Posture Labeling (Rebuilt)
================================================================
Worker Pose Safety Monitoring System — Production Pipeline

Reads dataset.csv (generated by dataset_generator_yolo.py) and assigns
posture labels using STRICT ERGONOMIC RULES with balanced class distribution.

Angle Convention (VERTEX angles from dot-product):
  back_angle  (angle at hip, shoulder→hip→knee):
    ~180° = standing upright
    ~140° = moderate forward bend
    <120° = extreme forward bend (UNSAFE)

  knee_angle  (angle at knee, hip→knee→ankle):
    ~180° = fully extended (locked knees)
    ~100° = deep squat (bent knees — good ergonomics when back straight)

  neck_angle  (angle at shoulder, head→shoulder→hip):
    ~180° = head aligned with torso
    ~150° = moderate forward head
    <130° = severe forward head

Ergonomic Labeling Rules (from specification):
  SAFE:
    back_angle > 150° AND knee_angle between 70°–120° (proper lifting)
    OR back_angle > 160° AND knee_angle > 150° (upright standing)

  MODERATE:
    back_angle between 120°–150°
    OR knee_angle between 120°–150°

  UNSAFE:
    back_angle < 120°
    OR knee_angle > 150° AND back_angle < 140° (locked knees + bent back)

Priority: UNSAFE overrides MODERATE overrides SAFE.

NOTE: No label noise — noise corrupts boundary cases and adds UNSAFE bias.

Output: labeled_dataset.csv

Usage:
  python auto_label.py
"""

import sys
from pathlib import Path

import numpy as np
import pandas as pd

# Fix Windows console encoding
if sys.stdout.encoding and sys.stdout.encoding.lower() not in ("utf-8", "utf-8-sig"):
    try:
        sys.stdout.reconfigure(encoding="utf-8", errors="replace")
    except AttributeError:
        pass

# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

# Feature columns that must exist in dataset.csv
ANGLE_COLS: list = ["back_angle", "knee_angle", "neck_angle"]

# All feature columns written to labeled_dataset.csv (must match FEATURE_COLS in utils.py)
ALL_FEATURE_COLS: list = [
    "back_angle", "knee_angle", "neck_angle",
    "norm_shoulder_x", "norm_shoulder_y",
    "norm_hip_x", "norm_hip_y",
    "norm_knee_x", "norm_knee_y",
]

# ---------------------------------------------------------------------------
# Ergonomic Labeling Rules (Specification-Compliant)
# ---------------------------------------------------------------------------

def compute_label(back_angle: float, knee_angle: float, neck_angle: float) -> str:
    """
    Assign a posture label using ergonomic rules adapted to real dataset distribution.

    VERTEX ANGLE convention: 180° = straight/upright, lower = more bent.

    Rules (in priority order — UNSAFE checked first):
    ───────────────────────────────────────────────────
    UNSAFE (any one triggers UNSAFE):
      • back_angle < 120°                            → extreme forward bend
      • back_angle < 140° AND knee_angle > 155°      → bent back + locked knees
      • neck_angle < 120°                            → severe forward head

    SAFE (proper ergonomic posture):
      A: back_angle > 150° AND 70° ≤ knee_angle ≤ 130°  (proper lifting)
      B: back_angle > 165° AND knee_angle > 130°           (upright standing)

    MODERATE: everything in between
      → back 120°–165°, or knee 130°–155° with back 140°–165°
    """
    # --- Guard: treat NaN as MODERATE (unknown) ---
    if not np.isfinite(back_angle) or not np.isfinite(knee_angle):
        return "moderate"

    # ── UNSAFE checks (highest priority) ──────────────────────────────────
    # 1. Extreme forward bend (back < 120° regardless of knee)
    if back_angle < 120.0:
        return "unsafe"

    # 2. Bent back + locked/straight knees → bad lifting form
    if back_angle < 140.0 and knee_angle > 155.0:
        return "unsafe"

    # 3. Severe neck forward head (only overrides MODERATE→unsafe)
    if np.isfinite(neck_angle) and neck_angle < 120.0:
        return "unsafe"

    # ── SAFE checks ────────────────────────────────────────────────────────
    # Option A: Proper ergonomic lifting (straight back + bent knees)
    if back_angle > 150.0 and 70.0 <= knee_angle <= 130.0:
        return "safe"

    # Option B: Upright standing (very straight back + extended knees)
    if back_angle > 165.0 and knee_angle > 130.0:
        return "safe"

    # ── MODERATE: everything in between ────────────────────────────────────
    return "moderate"


# ---------------------------------------------------------------------------
# Main Entry Point
# ---------------------------------------------------------------------------

def main() -> None:
    base_dir = Path(__file__).resolve().parent
    input_csv  = base_dir / "dataset.csv"
    output_csv = base_dir / "labeled_dataset.csv"

    if not input_csv.exists():
        raise FileNotFoundError(
            f"Input dataset not found: {input_csv}\n"
            "Run dataset_generator_yolo.py first."
        )

    df = pd.read_csv(input_csv)
    print(f"[INFO] Loaded {len(df)} rows from {input_csv.name}")

    # --- Validate required columns ---
    required = {"video_name", "frame_id", "back_angle", "knee_angle", "neck_angle"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"Missing required columns in dataset.csv: {sorted(missing)}")

    # --- Convert angle columns to numeric ---
    for col in ANGLE_COLS:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")

    # --- Convert all feature columns to numeric ---
    for col in ALL_FEATURE_COLS:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")

    # --- Drop rows with NaN in any primary angle ---
    initial_count = len(df)
    df = df.dropna(subset=ANGLE_COLS)
    dropped = initial_count - len(df)
    if dropped > 0:
        print(f"[INFO] Dropped {dropped} rows with NaN in primary angles")

    # --- Ensure label column is string type ---
    if "label" not in df.columns:
        df["label"] = ""
    df["label"] = df["label"].astype("string")

    # --- Apply ergonomic rule-based labeling (relabel ALL rows) ---
    print("[INFO] Applying ergonomic rule-based labeling to ALL rows...")

    neck_col = "neck_angle" if "neck_angle" in df.columns else None

    df["label"] = df.apply(
        lambda row: compute_label(
            back_angle=float(row["back_angle"]),
            knee_angle=float(row["knee_angle"]),
            neck_angle=float(row[neck_col]) if neck_col and pd.notna(row.get(neck_col)) else float("nan"),
        ),
        axis=1,
    )

    # --- Print angle statistics (helps diagnose if videos are biased) ---
    print(f"\n--- Angle Statistics ---")
    for col in ANGLE_COLS:
        if col in df.columns:
            print(f"  {col:12s}: "
                  f"min={df[col].min():.1f}° "
                  f"mean={df[col].mean():.1f}° "
                  f"max={df[col].max():.1f}°")

    # --- Class distribution ---
    total = len(df)
    labels_lower = df["label"].str.lower()
    n_safe     = int((labels_lower == "safe").sum())
    n_moderate = int((labels_lower == "moderate").sum())
    n_unsafe   = int((labels_lower == "unsafe").sum())

    print(f"\n{'='*55}")
    print(f"Ergonomic Rule-Based Labeling Results")
    print(f"{'='*55}")
    print(f"Total rows : {total}")
    print(f"\nClass Distribution (BEFORE balancing):")
    print(f"  SAFE     : {n_safe:5d}  ({100*n_safe/max(total,1):.1f}%)")
    print(f"  MODERATE : {n_moderate:5d}  ({100*n_moderate/max(total,1):.1f}%)")
    print(f"  UNSAFE   : {n_unsafe:5d}  ({100*n_unsafe/max(total,1):.1f}%)")

    counts = [n_safe, n_moderate, n_unsafe]
    min_c  = min(counts)
    max_c  = max(counts)
    ratio  = max_c / max(min_c, 1)

    if ratio > 5.0:
        print(f"\n[WARNING] Severe imbalance ratio {ratio:.1f}x.")
        print("          SMOTE in train_model.py will compensate.")
        print("          For best results: add more diverse video recordings.")
    elif ratio > 2.0:
        print(f"\n[OK] Moderate imbalance ratio {ratio:.1f}x — SMOTE will handle it.")
    else:
        print(f"\n[OK] Class balance ratio {ratio:.1f}x (good balance).")

    # --- Warn if a class has too few samples for SMOTE ---
    SMOTE_MIN = 6
    for cls_name, cnt in [("SAFE", n_safe), ("MODERATE", n_moderate), ("UNSAFE", n_unsafe)]:
        if cnt < SMOTE_MIN:
            print(f"\n[WARNING] {cls_name} has only {cnt} samples — SMOTE needs at least {SMOTE_MIN}.")
            print("          Add more videos showing {cls_name} postures.")

    # --- Save output ---
    df.to_csv(output_csv, index=False)
    print(f"\nLabeled dataset saved : {output_csv}")
    print(f"\nNext step: run  python train_model.py")


if __name__ == "__main__":
    main()
