"""
migrate_dataset.py — Dataset Migration Utility
===============================================
Worker Pose Safety Monitoring System

PURPOSE:
    The existing dataset.csv / labeled_dataset.csv were generated by the old
    MediaPipe pipeline. They use different column names and are missing the
    temporal features required by the new XGBoost training pipeline.

    OLD columns: shoulder_x, shoulder_y, hip_x, hip_y, knee_x, knee_y
    NEW columns: norm_shoulder_x, norm_shoulder_y, norm_hip_x, norm_hip_y,
                 norm_knee_x, norm_knee_y, back_vel, back_acc, knee_vel,
                 knee_acc, neck_vel

STRATEGY:
    This script runs a TARGETED YOLO re-extraction that replays the same
    frames stored in dataset.csv (by video_name + frame_id), avoids
    augmentation, and writes a fully-featured new dataset.csv.

    It is faster than dataset_generator_yolo.py because it only visits
    the exact frames already present in the CSV (no scanning every frame).

USAGE:
    python migrate_dataset.py

OUTPUT:
    Overwrites dataset.csv with the new 17-column format.
    Then run:  python auto_label.py
               python train_model.py
"""

import csv
import math
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import cv2
import numpy as np
import pandas as pd
from ultralytics import YOLO

from utils import (
    FEATURE_COLS,
    make_empty_buffers,
    select_primary_person,
    extract_all_features,
)

# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

YOLO_MODEL_NAME: str = "yolov8s-pose.pt"
FRAME_W: int = 640
FRAME_H: int = 480
YOLO_CONF_THR: float = 0.25

CSV_COLUMNS: List[str] = ["video_name", "frame_id"] + FEATURE_COLS + ["label"]


# ---------------------------------------------------------------------------
# Core: Re-extract features for specific frames
# ---------------------------------------------------------------------------

def extract_frames_for_video(
    video_path: Path,
    target_frame_ids: List[int],
    yolo_model: YOLO,
) -> List[Dict]:
    """
    Open a video, seek to each target frame_id, run YOLOv8, extract features.

    Args:
        video_path:       Path to .mp4 file
        target_frame_ids: Sorted list of frame indices to process
        yolo_model:       Pre-loaded YOLOv8 pose model

    Returns:
        List of feature dicts (rows), one per successfully processed frame.
    """
    rows = []
    cap = cv2.VideoCapture(str(video_path))
    if not cap.isOpened():
        print(f"  [WARN] Cannot open: {video_path.name}")
        return rows

    buffers = make_empty_buffers()
    target_set = set(target_frame_ids)
    frame_id = 0
    ok_count = 0
    fail_count = 0

    while True:
        ok, frame = cap.read()
        if not ok:
            break

        if frame_id not in target_set:
            frame_id += 1
            continue

        # Resize — no augmentation for migration (keep data clean)
        frame = cv2.resize(frame, (FRAME_W, FRAME_H), interpolation=cv2.INTER_LINEAR)

        results = yolo_model.predict(
            frame, verbose=False, conf=YOLO_CONF_THR, imgsz=FRAME_W
        )
        result = results[0]

        if (
            result.keypoints is None
            or result.boxes is None
            or len(result.boxes) == 0
        ):
            fail_count += 1
            frame_id += 1
            continue

        boxes = result.boxes.xyxy.cpu().numpy()
        kps_xy_all = result.keypoints.xy.cpu().numpy()
        kps_conf_all = result.keypoints.conf.cpu().numpy()

        person_idx = select_primary_person(boxes, FRAME_W, FRAME_H)
        if person_idx is None:
            fail_count += 1
            frame_id += 1
            continue

        kps_xy = kps_xy_all[person_idx]
        kps_conf = kps_conf_all[person_idx]

        feats = extract_all_features(kps_xy, kps_conf, buffers)
        if feats is None or any(math.isnan(v) for v in feats.values()):
            fail_count += 1
            frame_id += 1
            continue

        row = {"video_name": video_path.name, "frame_id": frame_id, "label": ""}
        row.update(feats)
        rows.append(row)
        ok_count += 1
        frame_id += 1

    cap.release()
    print(f"  [OK] {video_path.name}: {ok_count} rows re-extracted, {fail_count} failed")
    return rows


# ---------------------------------------------------------------------------
# CSV Writing
# ---------------------------------------------------------------------------

def write_dataset_csv(csv_path: Path, rows: List[Dict]) -> None:
    with csv_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=CSV_COLUMNS)
        writer.writeheader()
        for row in rows:
            clean = {col: row.get(col, "") for col in CSV_COLUMNS}
            writer.writerow(clean)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    base_dir = Path(__file__).resolve().parent
    old_csv = base_dir / "dataset.csv"
    output_csv = base_dir / "dataset.csv"  # overwrite in-place
    video_dir = base_dir / "Videos"
    if not video_dir.exists():
        video_dir = base_dir / "videos"

    # --- Validate inputs ---
    if not old_csv.exists():
        raise FileNotFoundError(
            f"dataset.csv not found at: {old_csv}\n"
            "Run dataset_generator_yolo.py first."
        )

    print(f"[INFO] Loading old dataset.csv ...")
    old_df = pd.read_csv(old_csv)
    print(f"[INFO] Old columns : {list(old_df.columns)}")
    print(f"[INFO] Old shape   : {old_df.shape}")

    # Check if migration is actually needed
    missing_cols = set(FEATURE_COLS) - set(old_df.columns)
    if not missing_cols:
        print("[INFO] dataset.csv already has all required columns. No migration needed.")
        print(f"       Required : {FEATURE_COLS}")
        return

    print(f"\n[INFO] Missing columns detected: {sorted(missing_cols)}")
    print("[INFO] Starting YOLO re-extraction for targeted frames...\n")

    # --- Group frames by video ---
    if "video_name" not in old_df.columns or "frame_id" not in old_df.columns:
        raise ValueError(
            "Old dataset.csv must have 'video_name' and 'frame_id' columns "
            "for re-extraction."
        )

    video_groups: Dict[str, List[int]] = {}
    for _, row in old_df.iterrows():
        vname = str(row["video_name"])
        fid = int(row["frame_id"])
        video_groups.setdefault(vname, [])
        video_groups[vname].append(fid)

    # Sort frame IDs per video for sequential access (faster seeking)
    for vname in video_groups:
        video_groups[vname].sort()

    print(f"[INFO] Videos to process   : {len(video_groups)}")
    print(f"[INFO] Total frames to seek : {sum(len(v) for v in video_groups.values())}")

    # --- Load YOLO ---
    model_path = base_dir / YOLO_MODEL_NAME
    if not model_path.exists():
        model_path = YOLO_MODEL_NAME  # try downloading
    print(f"\n[INFO] Loading {YOLO_MODEL_NAME} ...")
    yolo_model = YOLO(str(model_path))
    print("[INFO] Model loaded.\n")

    # --- Re-extract ---
    all_rows: List[Dict] = []
    missing_videos: List[str] = []

    for vname, frame_ids in video_groups.items():
        video_path = video_dir / vname
        if not video_path.exists():
            print(f"  [WARN] Video not found: {video_path}")
            missing_videos.append(vname)
            continue

        print(f"[INFO] Processing ({len(frame_ids)} frames): {vname}")
        rows = extract_frames_for_video(video_path, frame_ids, yolo_model)
        all_rows.extend(rows)

    # --- Write new CSV ---
    write_dataset_csv(output_csv, all_rows)

    print(f"\n{'='*60}")
    print(f"[DONE] Migration complete.")
    print(f"[DONE] New dataset rows : {len(all_rows)}")
    print(f"[DONE] New columns      : {CSV_COLUMNS}")
    print(f"[DONE] Saved to         : {output_csv}")

    if missing_videos:
        print(f"\n[WARN] {len(missing_videos)} videos not found (frames skipped):")
        for v in missing_videos:
            print(f"       - {v}")

    print(f"\n{'='*60}")
    print("Next steps:")
    print("  1. python auto_label.py      ← generates labeled_dataset.csv")
    print("  2. python train_model.py     ← trains XGBoost model")


if __name__ == "__main__":
    main()
